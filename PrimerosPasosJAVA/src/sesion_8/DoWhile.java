package sesion_8;

public class DoWhile {
    public static void main(String[] args) {

        //clclos normal
        var i = 1;
        do {
            System.out.println(i);
            i++;
        }while (i <= 20);

        var j = 20;
        do {
            System.out.println(j);
            j--;
        }while (j > 0);
    }
}
